#include "../hdrs/push_swap.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	if (!lst)
		return (0);
	i = 0;
	while (1)
	{
		i++;
		if (lst->last_el)
			break ;
		lst = lst->next;
	}
	return (i);
}
